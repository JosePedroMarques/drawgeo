package pt.drawgeo.map;


public class Challenge {
	

	
	private String piggies = null;
	private String numGuesses = null;
	private String description = null;
	private String password = null;
	private boolean check = false;
	/**
	 * @return the piggies
	 */
	public String getPiggies() {
		return piggies;
	}
	/**
	 * @param piggies the piggies to set
	 */
	public void setPiggies(String piggies) {
		this.piggies = piggies;
	}
	/**
	 * @return the numGuesses
	 */
	public String getNumGuesses() {
		return numGuesses;
	}
	/**
	 * @param numGuesses the numGuesses to set
	 */
	public void setNumGuesses(String numGuesses) {
		this.numGuesses = numGuesses;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the check
	 */
	public boolean isCheck() {
		return check;
	}
	/**
	 * @param check the check to set
	 */
	public void setCheck(boolean check) {
		this.check = check;
	}
	
	

}
